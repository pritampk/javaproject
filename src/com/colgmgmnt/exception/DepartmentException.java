package com.colgmgmnt.exception;

public class DepartmentException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6026159599704777705L;

	public DepartmentException() {
		super();
	}

	public DepartmentException(String msg, Throwable arg1) {
		super(msg, arg1);
	}

	public DepartmentException(String msg) {
		super(msg);
	}

}
